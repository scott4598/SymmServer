    static String polishVarName(Value value) {
	String v = value.toString();
	switch (v) {
	case "neg":
	case "cmp":
	case "cmpl":
	case "cmpg":
	case "checkpoint":
	case "monitorenter":
	case "output_t":
	case "output_T":
	case "output_l":
	case "output_L":
	case "exit":
	    return v + "$$";
	default:
	    return v;
	}
    }

