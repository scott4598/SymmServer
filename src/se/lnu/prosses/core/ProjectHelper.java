    public class SootMethodsComparator implements Comparator {
	@Override
	public int compare(Object o1, Object o2) {
	    if (((SootMethod) o1).isJavaLibraryMethod() && !((SootMethod) o2).isJavaLibraryMethod())
		return 1;
	    else if (((SootMethod) o2).isJavaLibraryMethod() && !((SootMethod) o1).isJavaLibraryMethod())
		return -1;
	    else
		return (((SootMethod) o1).getDeclaringClass().getName() + ((SootMethod) o1).getName())
			.compareTo(((SootMethod) o2).getDeclaringClass().getName() + ((SootMethod) o2).getName());
	}
    }

