    public void extractSourceSinksFromSecuritySignatures() {
	if (this.sourceSinkHelper.lowSinks.get(Utils.XMlMethod) == null)
	    this.sourceSinkHelper.lowSinks.put((String) Utils.XMlMethod, new ArrayList());
	if (this.sourceSinkHelper.lowSinks.get(Utils.XMLReference) == null)
	    this.sourceSinkHelper.lowSinks.put((String) Utils.XMLReference, new ArrayList());
	if (this.sourceSinkHelper.highSinks.get(Utils.XMLReference) == null)
	    this.sourceSinkHelper.highSinks.put((String) Utils.XMLReference, new ArrayList());
	if (this.sourceSinkHelper.highSinks.get(Utils.XMlMethod) == null)
	    this.sourceSinkHelper.highSinks.put((String) Utils.XMlMethod, new ArrayList());
	if (this.sourceSinkHelper.lowSources.get(Utils.XMlMethod) == null)
	    this.sourceSinkHelper.lowSources.put((String) Utils.XMlMethod, new ArrayList());
	if (this.sourceSinkHelper.highSources.get(Utils.XMlMethod) == null)
	    this.sourceSinkHelper.highSources.put((String) Utils.XMlMethod, new ArrayList());
	for (String method : this.securitySignatures.keySet()) {
	    List<SecuritySignature> signatures = securitySignatures.get(method);
	    for (SecuritySignature sig : signatures) {
		if (sig.secSignature.contains("return .*h") || sig.secSignature.contains("return *h")
			|| sig.secSignature.contains("return new(%(*))")) {
		    this.sourceSinkHelper.highSources.get(Utils.XMlMethod)
			    .add(new String[] { "L" + sig.className.replaceAll("\\.", "/"), sig.method });
		    Utils.log(getClass(), "Added " + sig.className + "." + sig.method + " as a HIGH source.");
		}

		if (sig.secSignature.contains("output_l")) {
		    if (sig.argsType.length > 0)
			this.sourceSinkHelper.lowSinks.get(Utils.XMlMethod)
				.add(new String[] { "L" + sig.className.replaceAll("\\.", "/"), sig.method });
		    else
			this.sourceSinkHelper.lowSinks.get(Utils.XMLReference)
				.add(new String[] { "L" + sig.className.replaceAll("\\.", "/"), sig.method });
		    Utils.log(getClass(), "Added " + sig.className + "." + sig.method + " as a LOW sink.");
		}

		if (sig.secSignature.contains("@!")) {
		    if (sig.argsType.length > 0)
			this.sourceSinkHelper.highSinks.get(Utils.XMlMethod)
				.add(new String[] { "L" + sig.className.replaceAll("\\.", "/"), sig.method });
		    else
			this.sourceSinkHelper.highSinks.get(Utils.XMLReference)
				.add(new String[] { "L" + sig.className.replaceAll("\\.", "/"), sig.method });
		    Utils.log(getClass(), "Added " + sig.className + "." + sig.method + " as a HIGH sink.");
		}

		if (sig.secSignature.contains("return new_?")) {
		    this.sourceSinkHelper.lowSources.get(Utils.XMlMethod)
			    .add(new String[] { "L" + sig.className.replaceAll("\\.", "/"), sig.method });
		    Utils.log(getClass(), "Added " + sig.className + "." + sig.method + " as a LOW source.");
		}

	    }
	}
    }

    /**
     * Load each class, iterate its methods, print signature + Jimple body (if
     * concrete).
     * 
     * @param symmariesHelper
     */
    private void processClasses(List<String> classNames, MethodsHelper symmariesHelper)
	    throws TypeTransformationException {
	// Pre-load a few core classes to stabilize Scene
	Scene.v().loadClassAndSupport("java.lang.Object");
	Scene.v().loadNecessaryClasses();

	for (String clsName : classNames) {
	    try {
		SootClass sootClass = Scene.v().loadClassAndSupport(clsName);
		// mark as application so Soot bothers materializing bodies where possible
		sootClass.setApplicationClass();
		Scene.v().loadNecessaryClasses();

		System.out.println();
		System.out.println("==================== CLASS: " + sootClass.getName() + " ====================");

		for (SootMethod m : sootClass.getMethods()) {
		    System.out.println();
		    System.out.println("---- Analyzing Method: " + m.getSignature() + " ----");
		    if (!symmariesHelper.processedMethods.contains(getMethodUniqueName(m, sootClass.getName()))) {
			symmariesHelper.processMethod(m, SootUtilities.getClassName(m.getDeclaringClass()));
		    }
		}

	    } catch (Exception e) {
		System.err.println("[error] Failed to load class " + clsName + ": " + e);
	    }
	}

    }

    public void processApplication(List<String> allClasses) {
	MethodsHelper symmariesHelper = new MethodsHelper(this, configurations,
		new TypeProcessor(configurations.typeHandlingConfig), sourceSinkHelper);
	try {
	    processClasses(allClasses, symmariesHelper);
	    generateOutputFiles(symmariesHelper);
	    symmariesHelper.typeProcessor.writeTypesClassesFile(configurations.targetDir);
	    // symmariesHelper.typeProcessor.writeTypeHierarchyDotFile(configurations.outputPath);
	} catch (TransformationException | TypeTransformationException e) {
	    Utils.logErr(JSymCompiler.class, "Aborted Processing the Application due to " + e.getLocalizedMessage());
	}

    }

